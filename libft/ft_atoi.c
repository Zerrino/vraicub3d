#include "libft.h"

int	ft_atoi(const char *str)
{
	int		i;
	long	result;
	long	last;
	int		sign;

	i = 0;
	result = 0;
	sign = 1;
	while (str[i] == ' ' || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sign *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		last = result;
		result = (result * 10) + (str[i++] - '0');
		if (result < last)
			return (~sign >> 1);
	}
	return ((int)result * sign);
}
